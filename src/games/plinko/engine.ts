export interface PlinkoResult {
  path: number[];      // 0=left, 1=right for each row
  finalBucket: number; // bucket index (0..rows)
  multiplier: number;
  win: number;
}

export function getMultipliers(rows: number, difficulty: Difficulty): number[] {
  return MULTIPLIERS[difficulty][rows] ?? MULTIPLIERS[difficulty][16];
}

/**
 * Simulate a single plinko drop deterministically using a seed string.
 * Returns the path, bucket, multiplier, and win amount.
 */
export function dropBall(
  bet: number,
  rows: number,
  difficulty: Difficulty,
  seed: string,
): PlinkoResult {
  const multipliers = getMultipliers(rows, difficulty);
  const path: number[] = [];

  // Use seed to drive direction decisions
  let hash = simpleHash(seed);
  for (let i = 0; i < rows; i++) {
    hash = xorshift(hash);
    path.push(hash % 2 === 0 ? 0 : 1);
  }

  // Count rights = bucket index
  const finalBucket = path.filter((d) => d === 1).length;
  const multiplier = multipliers[finalBucket] ?? 0;
  const win = Math.floor(bet * multiplier);

  return { path, finalBucket, multiplier, win };
}

function simpleHash(s: string): number {
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = (h * 0x01000193) >>> 0;
  }
  return h;
}

function xorshift(x: number): number {
  x ^= x << 13;
  x ^= x >> 17;
  x ^= x << 5;
  return x >>> 0;
}
